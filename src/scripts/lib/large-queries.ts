export const soldQuery = `
SELECT GVA12.COD_CLIENT as client_code,
GVA12.FECHA_EMIS as emission_date,
GVA12.MON_CTE,
GVA12.N_COMP,
GVA12.PORC_BONIF,
GVA12.COTIZ as dollar_price

FROM Compet_SA.dbo.GVA12 GVA12,
Compet_SA.dbo.GVA14 GVA14,
Compet_SA.dbo.GVA53 GVA53,
Compet_SA.dbo.STA11 STA11

WHERE GVA12.N_COMP = GVA53.N_COMP
AND GVA12.T_COMP = GVA53.T_COMP
AND GVA12.COD_CLIENT = GVA14.COD_CLIENT
AND GVA53.COD_ARTICU = STA11.COD_ARTICU
`;

